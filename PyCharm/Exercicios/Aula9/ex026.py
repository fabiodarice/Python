frase = str(input('Digite uma frase qualquer: '))
print('A letra "A" aparece {} vezes na frase'.format(frase.count('A')))
print('A Primeira vez em que aparece a letra "A" é na posição {}'.format(frase.find('A')))
print('A Última vez em que aparece a letra "A" é na posição {}'.format(frase.rfind('A')))


